package Basics;

public class updating_variables {
    public static void main(String[] args) {
        int a;
                a= 7;
        System.out.println(a);
        a =5;
        System.out.println(a);
        a = a + 6; // a +=6;
        System.out.println(a);
        a =a-2; // a -= 2;
        System.out.println(a);
        a = a * 5; // a *= 5;
        System.out.println(a);
        a = a/2;  // a /= 2;
        System.out.println(a);
    }
}
