package Basics;

public class arithmetic_Operations_datatype_int {
    public static void main(String[] args) {
        int x = 3 , y = 4;
        System.out.println(x+y); // 12
        System.out.println(x-y); // -1
        System.out.println(x*y); // 12
        System.out.println(x/y); // 0
    }
}
